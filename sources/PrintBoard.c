void	printBoard(int **board)
{
	printf("\n\t\t\t    ");	
	for (int col = 0; col < GRID_SIZE; col++)
	{
		printf(" %d  ", col + 1);
	}
	printf("\n");

	for (int row = 0; row < GRID_SIZE; row++)
	{
		if (row == 0)
		{
			printf("\t\t\t   ");
			for (int col = 0; col < GRID_SIZE; col++)
			{
				printf(" ---");
			}
			printf("\n");
		}
		printf("\t\t\t  %c", row + 'A');
		for (int col = 0; col < GRID_SIZE; col++)
		{
			printf("| %c ", board[row][col] == 0 ? ' ' : (board[row][col] == 1 ? 'X' : 'O'));
		}
		printf("|\n");

		printf("\t\t\t   ");
		for (int col = 0; col < GRID_SIZE; col++)
		{
			printf(" ---");
		}
		printf("\n");
	}
}
